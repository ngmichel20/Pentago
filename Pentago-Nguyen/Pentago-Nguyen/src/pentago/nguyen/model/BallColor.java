/**
 * This is the enumeration of BallColor.
 *
 * @author g48962
 */
enum BallColor {

    /**
     * Is the color black of a ball.
     */
    BLACK,
    /**
     * Is the color white of the ball.
     */
    WHITE,
    /**
     * Is "empty" if there is no ball on the board.
     */
    EMPTY;
}
